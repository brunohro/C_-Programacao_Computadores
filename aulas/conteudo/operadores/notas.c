#include <stdio.h>

int main(void) {
    double nota1, nota2;

    printf(" -> Digite a nota1: ");
    scanf("%lf",&nota1);
    
    printf(" -> Digite a nota2: ");
    scanf("%lf",&nota2);

    double media = (nota1 + nota2) / 2;

    if (media >= 7.0) {
        printf(" * PARABENS! APROVADO!");
    } else {
        // media < 7.0
        if (media < 4.0) {
            // media < 4.0
            printf(" * REPROVADO! TENTE OUTRA VEZ!");
        } else {
            // media >= 4.0 && media < 7.0
            printf(" * PROVA FINAL!");
        }
    }

    if (media < 4.0) {
        printf(" VOCE FOI REPROVADO!");
    } else {
        // media maior igual do que 4.0
        if (media >= 7.0) {
            printf( " VOCE FOI APROVADO!");
        } else {
            // media maior igual do que 4.0 e
            // media menor do que 7.0
            printf(" PROVA FINAL!");
        }
    }

    if (media >= 4.0 && media < 7.0) {
        printf(" PROVA FINAL!");
    } else {
        if (media >= 7.0) {
            printf(" APROVADO");
        } else {
            printf("REPROVADO");
        }
    }

    printf("\n * Media: %.1lf", media);

    return 0;
}
